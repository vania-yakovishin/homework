номер_дня = int(input("Будь ласка, введіть число від 1 до 7: "))
if номер_дня == 1:
    день_тижня = "Понеділок"
elif номер_дня == 2:

    день_тижня = "Вівторок"
elif номер_дня == 3:
    день_тижня = "Середа"
elif номер_дня == 4:
    день_тижня = "Четвер"
elif номер_дня == 5:
    день_тижня = "П'ятниця"
elif номер_дня == 6:
    день_тижня = "Субота"
elif номер_дня == 7:
    день_тижня = "Неділя"
else:
    день_тижня = "Невірний день тижня"
print("День тижня:", день_тижня)