перше_число = float(input("Будь ласка, введіть перше число: "))
друге_число = float(input("Будь ласка, введіть друге число: "))
if перше_число > друге_число:
    print("Більше число:", перше_число)
elif друге_число > перше_число:
    print("Більше число:", друге_число)
else:
    print("Числа рівні")